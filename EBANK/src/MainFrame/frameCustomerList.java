    private static Boolean addCustomer(java.lang.String name, java.lang.String doB, java.lang.String address, java.lang.String mobile, java.lang.String email, java.lang.String accountType, java.lang.String accountNumber, int sortCode, double balance, java.lang.String card) {
        customer_service.CustomerService_Service service = new customer_service.CustomerService_Service();
        customer_service.CustomerService port = service.getCustomerServicePort();
        return port.addCustomer(name, doB, address, mobile, email, accountType, accountNumber, sortCode, balance, card);
    }
    
    private static Boolean editCustomer(java.lang.String name, java.lang.String doB, java.lang.String address, java.lang.String mobile, java.lang.String email, java.lang.String accountNumber) {
        customer_service.CustomerService_Service service = new customer_service.CustomerService_Service();
        customer_service.CustomerService port = service.getCustomerServicePort();
        return port.editCustomer(name, doB, address, mobile, email, accountNumber);
    }
    
    private static Boolean deleteCustomer(java.lang.String accountNumber) {
        customer_service.CustomerService_Service service = new customer_service.CustomerService_Service();
        customer_service.CustomerService port = service.getCustomerServicePort();
        return port.deleteCustomer(accountNumber);
    }
    
