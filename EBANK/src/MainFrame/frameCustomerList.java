    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String Name = txtEditCusName.getText();
        String DoB = String.valueOf(jSpinYear1.getValue()) + "-" + String.valueOf(jSpinMonth1.getValue()) + "-" + String.valueOf(jSpinDate1.getValue());
        String Address = txtEditCusAddress.getText();
        String Mobile = txtEditCusMobile.getText();
        String Email = txtEditCusEmail.getText();
        String AccountNumber = txtCusAccNo.getText();
        
        if (Name.isEmpty() || DoB.isEmpty() || Address.isEmpty() || Mobile.isEmpty() || Email.isEmpty() || AccountNumber.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill the required fields ! ", "Warning Message", JOptionPane.WARNING_MESSAGE);
            //lblMessage.setText("Please Fill the required fields ! ");
        } else if (editCustomer(Name, DoB, Address, Mobile, Email, AccountNumber)) {
            JOptionPane.showMessageDialog(null, "Customer Details Succesfully Updated!", "Information Message", JOptionPane.INFORMATION_MESSAGE);
            //lblMessage.setText("Customer Succesfully Added!");
            clearData();
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong!", "Error Message", JOptionPane.ERROR_MESSAGE);
            //lblMessage.setText(" Something went wrong ! ");
            clearData();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void addACustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addACustomerActionPerformed
        // TODO add your handling code here:
        String Name = txtCusName.getText();
        String DoB = String.valueOf(jSpinYear.getValue()) + "-" + String.valueOf(jSpinMonth.getValue()) + "-" + String.valueOf(jSpinDate.getValue());
        String Address = txtCusAddress.getText();
        String Mobile = txtCusMobile.getText();
        String Email = txtCusEmail.getText();
        String accType = (String) cmbCardType.getSelectedItem();
        String AcountNumber = txtCusAccNum.getText();
        int sortCode = Integer.parseInt(txtCusSortCode.getText());
        Double Balance = Double.parseDouble(txtCusBalance.getText());
        String card = (String) cmbCard.getSelectedItem();
        
        if (Name.isEmpty() || DoB.isEmpty() || Address.isEmpty() || Mobile.isEmpty() || Email.isEmpty() || accType.isEmpty() || AcountNumber.isEmpty() || Balance.isNaN() || card.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill the required fields ! ", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (addCustomer(Name, DoB, Address, Mobile, Email, accType, AcountNumber, sortCode, Balance, card)) {
            JOptionPane.showMessageDialog(null, "Customer Succesfully Added!", "Information Message", JOptionPane.INFORMATION_MESSAGE);
            clearData();
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong!", "Error Message", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_addACustomerActionPerformed

