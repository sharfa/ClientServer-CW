    private void btnAddEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmployeeActionPerformed
        // TODO add your handling code here:
        String name = txtAddName.getText();
        String postion = txtAddPosition.getText();
        String username = txtAddUsername.getText();
        String password = txtAddPassword.getText();
        String passwordC = txtAddPasswordC.getText();

        if (!(name.isEmpty() || postion.isEmpty() || username.isEmpty() || password.isEmpty() || passwordC.isEmpty())) {

            if (password.equals(passwordC)) {
                if (addEmployee(name, postion, username, password)) {
                    lblMessage.setText(" Employee Registration Successful ! ");

                    clearData();
                } else {
                    lblMessage.setText(" Employee Registration Unsuccessful ! ");
                }

            } else {
                lblMessage.setText(" Password Mismatch ! ");
            }

        } else {
            lblMessage.setText(" Please Fillout the Employee Details ! ");
        }


    }//GEN-LAST:event_btnAddEmployeeActionPerformed

    private void btnUpdateEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmployeeActionPerformed
        // TODO add your handling code here:
        try{
        String idS = txtEditEmpid.getText();
        int id = Integer.parseInt(idS);
        String name = txtEditEmpName.getText();
        String position = txtEditEmpPosition.getText();
        String username = txtEditEmpUsername.getText();
        String password = txtEditEmppassoword.getText();
        String passwordC = txtEditEmppassowordC.getText();

        if (!(idS.isEmpty() || name.isEmpty() || position.isEmpty() || username.isEmpty() || password.isEmpty() || passwordC.isEmpty())) {
            if (password.equals(passwordC)) {
                if (editEmployee(id, name, position, username, password)) {
                    //lblEditMessage.setText(" Employee Update Successful ! ");
                    JOptionPane.showMessageDialog(null, "Employee Update Successful !");
                    clearData();
                } else {
                    // lblEditMessage.setText(" Employee Update Unsuccessful ! ");
                    JOptionPane.showMessageDialog(null, "Employee Update Unsuccessful !");
                }

            } else {
                // lblEditMessage.setText(" Password Mismatch ! ");
                JOptionPane.showMessageDialog(null, "Password Mismatch !");
            }
        } else {
            // lblEditMessage.setText(" Please Fillout the Employee Details ! ");
            JOptionPane.showMessageDialog(null, "Please Fillout the Employee Details !");
        }
        }catch(NumberFormatException | HeadlessException e){
        JOptionPane.showMessageDialog(null, " Please Fillout the Form ! ");
        }
    }//GEN-LAST:event_btnUpdateEmployeeActionPerformed

    private void btnEmpDelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpDelete1ActionPerformed
        // TODO add your handling code here:
        String idS = txtDelEmp.getText();
        try {
            int id = Integer.parseInt(idS);
            if (!(idS.isEmpty())) {
                deleteOperation(id);
                JOptionPane.showMessageDialog(null, "Employee Successfully Deleted !");
                //lblDeleteMessage.setText(" Employee Successfully Deleted ! ");
                clearData();
            } else {
                JOptionPane.showMessageDialog(null, "Please Fill out the employee ID");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, " Invalid ID ! ");
        }
    }//GEN-LAST:event_btnEmpDelete1ActionPerformed

