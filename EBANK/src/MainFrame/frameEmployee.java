    private static Boolean addEmployee(java.lang.String name, java.lang.String position, java.lang.String username, java.lang.String password) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.addEmployee(name, position, username, password);
    }

    private static Boolean editEmployee(int id, java.lang.String name, java.lang.String position, java.lang.String username, java.lang.String password) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.editEmployee(id, name, position, username, password);
    }

    private static Boolean deleteOperation(int id) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.deleteOperation(id);
    }

